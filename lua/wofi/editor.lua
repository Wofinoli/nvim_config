-- Sets line numbers
vim.opt.number = true

-- Expands tabs to spaces
vim.opt.expandtab = true

-- Width of tab
vim.opt.tabstop = 2
vim.opt.shiftwidth = 0 -- Sets < and > To move the same length as tab stop

-- Search settings
vim.opt.incsearch = true
vim.opt.hlsearch = false

-- Start without folds
vim.opt.foldlevelstart = 99

-- colors
vim.opt.termguicolors = true

-- Keep some lines at the end
vim.opt.scrolloff = 8

-- Update time
vim.opt.updatetime = 50
